    def setup(self):

        for j in range(3):
            for i in self.lijst_lijnen:
                GPIO.output(self.chip_select, GPIO.LOW)
                for k in test3[(i-1)%8]:
                    self.spi.writebytes([0xF, 0x00])
                GPIO.output(self.chip_select, GPIO.HIGH)

        for j in range(3):
            for i in self.lijst_lijnen:
                GPIO.output(self.chip_select, GPIO.LOW)
                for k in test3[(i-1)%8]:
                    self.spi.writebytes([0xB, self.scanLimiet]) # Zet het Scan Limit register (0xB) op 0x7 zodat alle 8 rijen kunnen worden gebruikt.
                GPIO.output(self.chip_select, GPIO.HIGH)

        for j in range(3):
            for i in self.lijst_lijnen:
                GPIO.output(self.chip_select, GPIO.LOW)
                for k in test3[(i-1)%8]:
                    self.spi.writebytes([0x9, self.decodeMode]) # Schrijf voor alle zekerheid een 0x0 naar register 0x9 (want we gebruiken maar 1 dot matrix)
                GPIO.output(self.chip_select, GPIO.HIGH)

        for j in range(3):
            for i in self.lijst_lijnen:
                GPIO.output(self.chip_select, GPIO.LOW)
                for k in test3[(i-1)%8]:
                    self.spi.writebytes([0xC,self.shutdown])# Plaats een 1 in het Shutdown-register (display aanleggen) en dan kan je de display beginnen gebruiken 
                    # door per rij een overeenkomstige byte naar het juiste register (0x1-0x8) te schrijven. 
                GPIO.output(self.chip_select, GPIO.HIGH)

        for j in range(3):
            for i in self.lijst_lijnen:
                GPIO.output(self.chip_select, GPIO.LOW)
                for k in test3[(i-1)%8]:
                    self.spi.writebytes([0xA, self.intensiteit])        
                GPIO.output(self.chip_select, GPIO.HIGH)


    def op_matrix_zetten(self, input):
        for j in range(1):
            for i in self.lijst_lijnen:
                # time.sleep(0.01)
                GPIO.output(self.chip_select, GPIO.LOW)
                for k in input[(i-1)%8]:
                    # time.sleep(0.01)
                    self.spi.writebytes([i, k])
                    # time.sleep(0.01)
                GPIO.output(self.chip_select, GPIO.HIGH)


    def gegeven_delen_op_matrix_zetten(self, deel1, deel2):
        samen = []
        for i in range(8):
            # 8 want er zitten 8 lijsten in alles_1 = len(alles_1)
            tussen = deel1[i][:] # B V1-4    vb: ["B V1 R1 - 11", "B V2 R1 - 12","B V3 R1 - 13", "B V4 R1 - 14"]
            tussen.extend(deel2[i]) # O V1-4 toevoegen aan B V1-4  => [B V1-4 , O V1-4]   => ['B V1 R1 - 11', 'B V2 R1 - 12', 'B V3 R1 - 13', 'B V4 R1 - 14', 'O V1 R1 - 15', 'O V2 R1 - 16', 'O V3 R1 - 17', 'O V4 R1 - 18']
            samen.append(tussen) # [...] => [... , [B V1-4 ,, O V1-4] ]

        self.op_matrix_zetten(samen)


